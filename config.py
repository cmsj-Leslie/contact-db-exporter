# ============================================================
#  配置文件 —— 使用前请修改这里，改完保存即可
# ============================================================
#
# 这是整套工具的唯一配置入口。extract_key.py 和 export_contacts.py
# 都从这里读取配置，你只需要改这一个文件。
#
# 用 r"..." 原始字符串写 Windows 路径，避免反斜杠转义问题。
# ============================================================


# ── 1. 微信数据库目录（必填）─────────────────────────────────
#
# 指向你的 db_storage 文件夹，注意结尾不带 \contact\contact.db。
#
# 怎么找：
#   1) 打开微信 → 设置 → 通用 → 文件管理，看“当前微信文件保存位置”
#   2) 进入该目录下的 xwechat_files\<wxid_xxxx>\db_storage
#   3) <wxid_xxxx> 是形如 wxid_abc123... 的文件夹；多个账号会有多个，
#      认准“当前正在登录”的那个账号的 wxid
#
# 示例：
#   DB_DIR = r"D:\xwechat_files\wxid_abc123def456\db_storage"
DB_DIR = r"在这里填写你的 db_storage 路径"


# ── 2. 数据库密钥（可留空，由 extract_key.py 自动填）──────────
#
# 64 个十六进制字符的 raw key。
# 留空时：运行 extract_key.py 会自动从内存提取并写入 key.txt，
#         export_contacts.py 会自动从 key.txt 读取，无需手填。
# 也可以手动粘贴已知的 key 到这里（优先级高于 key.txt）。
ENC_KEY_HEX = ""


# ── 3. 输出目录（必填）───────────────────────────────────────
#
# 提取的 key.txt、解密后的数据库、导出的 CSV 都放这里。
# 用 "." 表示脚本所在目录。
OUTPUT_DIR = "."


# ── 4. 账号名称（用于给精简版好友列表命名）──────────────────
#
# 运行后会导出两份 CSV：
#   contacts.csv            —— 全量：全部联系人 + 全部原始字段
#   <账号名>的好友列表.csv   —— 精简：仅真实好友 + 中文表头
#
# 这里填的名字只用于拼上面那个精简版文件名，随便填好认的即可，
# 比如你的微信号、备注名。程序读不到微信号，所以需要手动填。
# 示例：ACCOUNT_NAME = "txsp668"  →  生成 txsp668的好友列表.csv
ACCOUNT_NAME = "我的微信"


# ── 5. 微信进程名（一般不用改）──────────────────────────────
#
# 微信 4.x 进程名是 Weixin.exe；老版本 3.x 是 WeChat.exe。
WECHAT_PROCESS = "Weixin.exe"
